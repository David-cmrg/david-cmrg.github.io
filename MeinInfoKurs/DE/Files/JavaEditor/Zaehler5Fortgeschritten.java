public class Zaehler5Fortgeschritten {
  public static void main(String[] args) {
    int Zaehler=0;
  
    System.out.println("Es wird von 5 runtergezählt");
    System.out.println("");
    System.out.println(Zaehler);
    while (Zaehler<5) { 
      Zaehler++;
      System.out.println(Zaehler);  
    } // end of while    
    
  }
}
