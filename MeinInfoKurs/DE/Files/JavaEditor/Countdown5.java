public class Countdown5 {
  public static void main(String[] args) {
    int Zaehler=5;
  
    System.out.println("Es wird von 5 runtergezählt");
    System.out.println("");
    System.out.println(Zaehler);
    while (Zaehler>0) { 
      Zaehler--;
      System.out.println(Zaehler);  
    } // end of while    
    
  }
}
